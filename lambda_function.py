def reflect(fragment):
    tokens = fragment.lower().split()
    for i, token in enumerate(tokens):
        if token in reflections:
            tokens[i] = reflections[token]
    return ' '.join(tokens)


def analyze(statement):
    for pattern, responses in psychobabble:
        match = re.match(pattern.lower(), statement.lower().rstrip(".!"))
        if match:
            response = random.choice(responses)
            return response.format(*[reflect(g) for g in match.groups()])


def eliza_response(intent,session):
    ## main intent manager
    session_attributes = {}
    reprompt_text = None
    dumpclean(intent)
    if session['new']:
        return get_welcome_response()
    elif intent['slots']['patient'].has_key("value") and intent['slots']['patient']['value'] in signoff:
        should_end_session = True
        speech_output = random.choice(toods)
        return build_response(session_attributes, build_speechlet_response(
            intent['name'], speech_output, reprompt_text, should_end_session))
    else:
        if(intent['slots']['patient'].has_key("value")==False):
            raise ValueError("intent['slots']['patient'].has_key('value')==False Booo Hiss")
        speech_output = analyze(intent['slots']['patient']['value'])
        reprompt_text = "Sorry you'll have to speak up. " + speech_output
        should_end_session = False
        
        return build_response(session_attributes, build_speechlet_response(
            intent['name'], speech_output, reprompt_text, should_end_session))
        
